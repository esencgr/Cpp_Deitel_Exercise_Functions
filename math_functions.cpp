#include<iostream>
#include<math.h>
using namespace std;
int main(){
    cout << " sqrt(9.0)="    << sqrt(9.0)     <<endl; 
    cout << " exp(1.0)="     << exp(1.0)      <<endl;
    cout << " exp(2.0)="     << exp(2.0)      <<endl;
    cout << " log(2.71828)=" << log(2.718)    <<endl;
    cout << " log10(1.0)="   << log10(1.0)    <<endl;
    cout << " log10(10.0)="  << log10(10.0)   <<endl;
    cout << " fabs(-10)="    << fabs(-10)     <<endl;
    cout << " ceil(9.2)="    << ceil(9.2)     <<endl;
    cout << " ceil(-9.8)="   << ceil(-9.8)    <<endl;
    cout << " floor(9.2)="   << floor(9.2)    <<endl;
    cout << " floor(-9.8)="  << floor(-9.8)   <<endl;
    cout << " pow(2,5)="     << pow(2,5)      <<endl;
    cout << " fmod(13,8)="   << fmod(13,8)    <<endl;
    cout << " sin(0)="       << sin(0)        <<endl;
    cout << " cos(0)="       << cos(0)        <<endl;
}